package com.dlz.comm.consts;

/**
 * 字符常量接口
 * 
 * 定义常用的字符常量，避免硬编码
 * 
 * @author dingkui
 * @since 2023
 */
public interface Char {
    /** 大写字母 A */
    char UPPER_A = 'A';
    /** 小写字母 a */
    char LOWER_A = 'a';
    /** 大写字母 Z */
    char UPPER_Z = 'Z';
    /** 小写字母 z */
    char LOWER_Z = 'z';
    /** 点号 */
    char DOT = '.';
    /** 电子邮件符号 */
    char AT = '@';
    /** 左花括号 */
    char LEFT_BRACE = '{';
    /** 右花括号 */
    char RIGHT_BRACE = '}';
    /** 左圆括号 */
    char LEFT_BRACKET = '(';
    /** 右圆括号 */
    char RIGHT_BRACKET = ')';
    /** 破折号 */
    char DASH = '-';
    /** 百分号 */
    char PERCENT = '%';
    /** 管道符 */
    char PIPE = '|';
    /** 加号 */
    char PLUS = '+';
    /** 问号 */
    char QUESTION_MARK = '?';
    /** 感叹号 */
    char EXCLAMATION_MARK = '!';
    /** 等号 */
    char EQUALS = '=';
    /** 和号 */
    char AMPERSAND = '&';
    /** 星号 */
    char ASTERISK = '*';
    /** 星号（别名） */
    char STAR = ASTERISK;
    /** 反斜杠 */
    char BACK_SLASH = '\\';
    /** 冒号 */
    char COLON = ':';
    /** 逗号 */
    char COMMA = ',';
    /** 美元符号 */
    char DOLLAR = '$';
    /** 正斜杠 */
    char SLASH = '/';
    /** 井号 */
    char HASH = '#';
    /** 插入符号 */
    char HAT = '^';
    /** 左尖括号 */
    char LEFT_CHEV = '<';
    /** 换行符 */
    char NEWLINE = '\n';
    /** 小写字母 n */
    char N = 'n';
    /** 小写字母 y */
    char Y = 'y';
    /** 双引号 */
    char QUOTE = '\"';
    /** 回车符 */
    char RETURN = '\r';
    /** 制表符 */
    char TAB = '\t';
    /** 右尖括号 */
    char RIGHT_CHEV = '>';
    /** 分号 */
    char SEMICOLON = ';';
    /** 单引号 */
    char SINGLE_QUOTE = '\'';
    /** 反引号 */
    char BACKTICK = '`';
    /** 空格 */
    char SPACE = ' ';
    /** 波浪号 */
    char TILDA = '~';
    /** 左方括号 */
    char LEFT_SQ_BRACKET = '[';
    /** 右方括号 */
    char RIGHT_SQ_BRACKET = ']';
    /** 下划线 */
    char UNDERSCORE = '_';
    /** 数字 1 */
    char ONE = '1';
    /** 数字 0 */
    char ZERO = '0';
}